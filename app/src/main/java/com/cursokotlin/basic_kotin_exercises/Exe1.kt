package com.cursokotlin.exercicioskotlin

fun main() {
    /*
    Exercício sobre variáveis e tipos de dados

    Exercise about variables and types of data
     */
    val message = "Android Masterclass"
    print("String: $message\n")
    val myFloat: Float = 13.37F
    print("Float: " + myFloat + "\n")
    val myDouble: Double = 3.14159265358979323846
    print("Double: " + myDouble + "\n")
    val myByte: Byte = 25
    print("Byte: " + myByte + "\n")
    val myShort: Short = 2020
    print("Short: " + myShort + "\n")
    val myLong: Long = 18881234567
    print("Long: " + myLong + "\n")
    val myBoolean = true
    print("Boolean: " + myBoolean + "\n")
    val myChar = 'a'
    print("Char: " + myChar)
}